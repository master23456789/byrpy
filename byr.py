#WAP to enter 2 numbers from the user and check whether the 1st no. is divisible by 3 or not while check the other no. which is divisible by 8 or not.
k = int(input('The 1st number by the user: '))
if k % 3 == 0:
    print('The number is divisible by 3.')   
elif k % 8 == 0:
    print('The number is divisible by 8.')
    
r = int(input('The second number by the user: ')) 
if r % 3 == 0:
    print('The number is divisible by 3.')
elif r % 8 == 0:
    print('The number is divisible by 8.')
  
 
    
       
    







